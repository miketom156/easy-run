package com.job5156.vo.api;

/**
 * 简历搜索查询结果数量统计
 * 
 * @author leo
 * 
 */
public class ResumeSearchCountResult extends BaseResult {

	private Integer realCount; 			// 真实结果数量
	private ResumeSearchFilter filter; 	// 分类统计数量

	public Integer getRealCount() {
		return realCount;
	}

	public void setRealCount(Integer realCount) {
		this.realCount = realCount;
	}

	public ResumeSearchFilter getFilter() {
		return filter;
	}

	public void setFilter(ResumeSearchFilter filter) {
		this.filter = filter;
	}
}
